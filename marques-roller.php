								<a href="http://valo-brand.com/"><img src="public/img/valo.jpg" alt="Valo Skates" class="photoarticle" id="V" title="vers le site"/></a><strong>Valo</strong>: c'est la marque de Street, de la légende Jon Julio, qui après avoir fait une brillante carrière de rider, ne cesse de s'investir dans le développement du Street. Créée en 2003, Valo se distingue par son design et l'overboot qui recouvre ses patins.
								<br/>Modèles phares: <strong>TV1, Loco Skates, Alex Broskow</strong>,...
							</p>
