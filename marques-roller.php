							<p>
								<img src="public/img/oxelo.jpg" alt="Oxelo" id="O" class="photoarticle"/></a><strong>Oxelo</strong>: c'est la marque de glisse urbaine de Décathlon, créée en 2007, elle propose des rollers de tous types, des skateboards et des trottinettes. Elle est plutôt orientée sur des produits pour les débutants, avec des prix très attractifs , mais les produits gagnent en qualité, et la marque s'investit dans le développement des sports urbains. 
								<br/>Modèles phares: <strong>Diabolo, FIT, Street</strong>,...
