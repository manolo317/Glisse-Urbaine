								<a href="http://www.shimamanufacturing.com/"><img src="public/img/shimaskates.jpeg" alt="Shima skates" class="photoarticle" title="vers le site"/></a><strong>Shima skates</strong>: marque de Brian Shima, un de meilleurs riders des années 2000, qui a lancé sa marque en 2011, pour construire les rollers qu'il a envie de rider et surtout continuer à participer au développement du Street.
								<br/>Modèles phares: <strong>Montre Livingston Encore, Marc Moreno, Bolino</strong>.
