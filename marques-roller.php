								<a href="http://www.trailskate.net/fr/index.php"><img src="public/img/trailskate.jpg" alt="Trailskate" class="photoarticle" title="vers le site"/></a><strong>Trailskate</strong>: marque de roller tout-terrain française, proposant un concept de roller se rapprochant du ski, avec un long chassis en composite et deux grandes roues gonflables. Ca passe sur tous les types de terrains, c'est aussi fiable côté matériel avec un frein hydraulique pour assurer le coup dans les descentes.
							</p>
