							<p>
								<a href="http://remz.com/"><img src="public/img/remz.gif" alt="Remz" class="photoarticle" title="vers le site"/></a><strong>Remz</strong>: marque américaine de patins de Street, créée en 1998 par Kato Mateu, qui le premier a eu l'idée de construire un "non-skiboots skates", c'est à dire sans spoiler externe et sans boucles: Le Remedyz. La marque Remz a apporté de nombreuses innovations au roller agressif (Soft Boot, Spoiler V-cut,...) et reste aujourd'hui une référence.
								<br/>Modèles phares: <strong>The Remedyz, Haffey, HR1</strong>,...
							</p>
							<br/><br/>
							<p>
								<a href="http://www.roller.riedellskates.com/"><img src="public/img/riedell.jpg" alt="Riedell" class="photoarticle" title="vers le site"/></a><strong>Riedell</strong>: marque de roller quad et de patins sur glace américaine et leader du marché dans le Roller Derby. Elle propose une large gamme de roller de Derby, sport qui se développe fortement ces dernières années, qui va du patin d'entrée de gamme avec boot en simili cuir et platine en nylon, au patin haut de gamme en vrai cuir et platine ultra technique en aluminium extrudé: pour tous les niveaux et toutes les bourses!
								Elle se démarque par ses excellentes chaussures, qui offrent un très bon maintien et une bonne durée de vie. Les patins sont équipés de platines Powerdyne et de roues Radar.
								<br/>Modèles phares: <strong>R3, Dash, Vixen, Blue Streak</strong>,...
							</p>
							<br/>
							<p>
								<a href="http://www.roces.com/"><img src="public/img/roces.jpg" alt="Roces" class="photoarticle" title="vers le site"/></a><strong>Roces</strong>: Marque italienne pionnière dans le marché du roller, elle fait parti avec Rollerblade, des marques historiques dans l'invention et le développement du roller en ligne.
								<br/>Modèles phares: <strong>Majestic 12, Barcelona, Orlando, Big Cat</strong>,...
							</p>
