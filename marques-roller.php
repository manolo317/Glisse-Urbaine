								<br/>Modèles phares: <strong>FSK, Hardcore Evo, Virus</strong>...
							</p>
							<br/>
							<p>
								<a href="http://www.razorskate.com/"><img src="public/img/razors.jpg" alt="Razors" id="R" class="photoarticle" title="vers le site"/></a><strong>Razors</strong>: marque américaine créée par Andy Wegener en 1989, elle fait partie des marques historiques de roller, spécialisée dans le patins de Street, elle fait partie des marques leaders du marché avec USD.
								<br/>Modèles phares: <strong>Genesys, SL, Cult, Brian Shima</strong>...
							</p>
