								<a href="http://www.roll-line.it/"><img src="public/img/roll-line.jpg" alt="Roll line" class="photoarticle" title="vers le site"/></a><strong>Roll line</strong>: marque de roller italienne, spécialisée dans le roller quad artistique et Derby. Elle s'est mis depuis peu à faire des roues et des platines de roller en ligne de Powerblading. Du super matériel, platines avec une finition impeccable et de très bons roulements.
								<br/>Modèles phares: <strong>Platine Variant, Platine Skill et Cage, roulements kit micro</strong>,...
