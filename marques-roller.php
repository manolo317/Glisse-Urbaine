							<p>
								<a href="http://www.undercover-wheels.com/"><img src="public/img/undercover.jpg" alt="Undercover wheels" id="U" class="photoarticle" title="vers le site"/></a><strong>Undercover</strong>: marque de roues de Street américaine, propose une très large gamme et une très bonne qualité de gomme. Fait parti de <a href="http://theconference.org/" title="lien vers The Conference">La Conférence</a> (<a href="powerslide.php" id="Powerslide">Powerslide</a>).
							</p>
							<br/><br/><br/>
							<p>
								<a href="http://www.usd-skate.com/"><img src="public/img/usd.jpg" alt="USD" class="photoarticle" title="vers le site"/></a><strong>USD</strong>: Universal Skate Design est une marque de roller agressif, créée en 1997, elle a participé au développement technique et populaire de la pratique Street, avec le système UFS notamment et ses riders stars, comme Jon Julio, Brian Shima ou Dominic Sagona. La marque fait parti de <a href="http://theconference.org/" title="lien vers The Conference">La Conférence</a>, qui regroupe les marques de Street, Xsjado, Kizer et Undercover, et qui appartient au groupe <a href="#" id="Powerslide">Powerslide</a>.
								<br/>Modèles phares: <strong>Psirus, Throne, Carbon</strong>....
							</p>
