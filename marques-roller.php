							<p>
								<a href="http://www.rollerblade.com/france/"><img src="public/img/rollerblade.jpg" alt="Rollerblade" class="photoarticle" title="vers le site"/></a><strong>Rollerblade</strong>: Marque américaine créée en 1983 par Scott et Brennan Olson, c'est à eux que revient le mérite de l'invention de la pratique de roller en ligne. Ils sont leader depuis plus de 15 ans sur le marché du Fitness. Type de roller: Fitness, Freeskate, Enduro et Street. La marque fait maintenant partie du groupe Tecnica (ski). 
