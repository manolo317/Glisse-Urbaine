							<p>
								<a href="http://mpcspeedskating.com/"><img src="public/img/mpc.png" alt="MPC wheels" class="photoarticle" title="vers le site"/></a><strong>MPC</strong>: marque de roues de vitesse haut de gamme américaine. Fait partie des leaders avec Matter, Atom et Hyper.
