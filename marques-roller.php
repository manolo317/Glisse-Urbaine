								<a href="http://www.sebaskates.com/"><img src="public/img/seba.jpg" alt="Seba" class="photoarticle" title="vers le site"/></a><strong>Seba</strong>: marque française de Freeskate, créée par Sébastien Laffargue, champion de Slalom (freestyle et vitesse) et de descente, en 2004. Elle s'est imposée en quelques années, comme la référence en patins de Freeskate, avec des produits techniques et très performants, elle développe maintenant d'autres pratiques comme la vitesse, le fitness ou le street. 
								En plus d'être devenu une très bonne marque de roller, Seba s'investit dans le milieu du roller, avec la promotion et l'organisation de nombreuses pratiques (Slalom, Skate Cross, Freeskate, Downhill,...)
								<br/>Modèles phares: <strong>FR1, Igor, GT, Trix</strong>,...
							</p>
