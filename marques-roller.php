								<strong>Nimh</strong>: première marque de patins de Street de Brian Shima, un de meilleurs riders des années 2000 et Jon Elliot (créateur de Ground Control), créée en 2007. Elle s'arrête en 2011, quand Brian Shima lance sa marque Shima Skates.
