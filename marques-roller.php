								Ils proposaient des rollers de fitness et de descente notamment, de bonnes qualités, notamment des Modèles avec platines démontables, comme la marque Hypno.
								<br/>Modèles phares: <strong>Descender, Step in</strong>,...
							</p>
							<br/>
							<p>
								<img src="public/img/salomon.jpg" alt="Salomon" id="S" class="photoarticle"/></a><strong>Salomon</strong>: marque de matériel outdoor (ski, randonnée, alpinisme,...) et ancienne marque de roller française. Le groupe Salomon a été vendu par Adidas en 2005, à Amer Sports, qui a stoppé l'activité roller, la jugeant pas assez rentable... Salomon proposait pourtant d'excellents patins avec un confort inégalable et des innovations techniques qui ont été reprises par d'autres marques (platine UFS, chausson thermomoulable, invention de la coque du Xsjado,...).
								<br/>Modèles phares:  <strong>Motion, FSK, ST90</strong>,...
							</p>
