				<p>
					Longtemps confidentiel le longboard séduit de plus en plus d'adeptes, recherche de nouvelles sensations de glisse, de vitesse ou simplement un moyen de se déplacer, les pratiques et pratiquants sont très diversifiés et les marques et les modèles des fabricants sont tout aussi nombreux.
					Donc avant de se lancer dans l'aventure, il est important de bien déterminer ce que l'on a envie de faire avec notre planche, cela va ainsi conditionner le choix du matériel.
					Avec le choix pléthorique de marques et de types de longboards, je vais essayer d'éclairer les novices sur les différents matériels et les diffèrentes pratiques de longboard.
				</p>
			</div>
			
			<div id="corps">
				<ul>
					<li>
						<div class="para_article">
								
								<h3>Anatomie d'un longboard:</h3>
								<h3>Le plateau:</h3>
					
							<ul>
								<li><strong>Matière</strong>: la plupart des plateaux sont composés de couches d'érable canadien, mais on trouve ces dernières années de nouveaux matériaux qui apportent d'autres propriétés, comme le bambou, qui apporte de la flexibilité ou de la fibre de verre, de carbone et de plastique, qui apporte aussi de la flexibilité et de la solidité. La matière du plateau conditionne beaucoup le comportement de la board ainsi que sa longévité.</li><br/>
								<li><strong>Forme</strong>: il existe une grande variété de forme de plateaux, toutes caractéristiques de leurs pratiques.<br/></li><br/>
								
								<p>
									<img src="public/img/decklongboard.jpg" alt="plateaux" class="photopage"/>
								</p>
								<p><strong>Le pintail</strong>: la forme classique du longboard, qui rappelle la forme du surf, un plateau idéal pour du cruising et du carving.<br/></p>
								<p><strong>Downhill</strong>: Des plateaux épais et bien lourd pour la descente.<br/></p>
								<p><strong>Twin-tip</strong>: des planches symétriques, avec les passages de roues découpés, pour un usage polyvalent, freeride.<br/></p>
								<p><strong>Street</strong>: la même forme que les planches de Street, avec un tail pour lever la planche et faire des figures.<br/></p>
								<p><strong>Freestyle</strong>: un plateau avec un "nose" et un "tail", pour pouvoir faire des figures.<br/></p></li>
								<br/>
								<li><strong>Concave</strong>: le concave peut être positif ou négatif, sur les boards de carving et de slalom il est positif pour donner de la nervosité à la planche et permettre de "pomper".
									Sur les boards de freeride, descente ou street, il est négatif, on appelle ça aussi le "rocker", ça permet d'avoir les pieds bien fixé à la planche, de gagner en stabilité et en contrôle.
									Il est plat pour les boards de dancing.
								</li><br/>
								<li><strong>Taille</strong>: la taille influence la maniabilité et la stabilité de votre longboard, plus l'empattement entre les trucks est long, moins la planche tournera rapidement. La largeur de la planche jouera sur la stabilité, à vous de trouver la taille qui correspondra à vos attentes et votre gabarit.</li><br/>
								<li><strong>Flex</strong>: la flexibilité permet un confort appréciable pour faire du cruising ou du carving, elle permet d'absorber les petits défauts de la route, mais aussi de "pomper" et ainsi relancer votre board sans avoir à pousser. Par contre, il faut mieux une planche rigide pour les disciplines exigeants de la réactivité et de la précision (freeride, descente, slalom).</li>
							</ul>
						</div>
					</li>
				</ul>
				<ul>
					<li>
						<div class="para_article">
							<h3>Trucks et montage:</h3>
					
							<p>
								Les trucks vont joués sur la stabilité et la maniabilité de la planche, d'abord par leur largeur, en effet, plus un truck est large, plus il est stable, sa taille varie en fonction de la largeur du plateau, entre 150 mm et 195 mm.<br/> Ensuite, il va y avoir l'angle que fait le truck avec la planche qui va jouer, plus l'angle est élevé, plus le rayon de braquage sera important et donc la planche tournera plus facilement. Par contre elle perdra en stabilité à grande vitesse, donc pour une board stable, préférez des trucks avec un angle faible (entre 40 et 44°) et donc pour du carving des trucks avec un grand angle (50°).
								<br/>Autre point des trucks qui joue sur le comportement de la planche, ce sont les gommes. Il en existe une multitude, qui sont biens spécifiques aux types de trucks. Pour faire court, plus la gomme est tendre, plus la trucks seront maniable et plus elles sont dures et écrasées, plus la direction est dure et donc la planche est plus stable.<br/>
								Enfin, dernier point, c'est le montage des trucks sur la planche, il en existe plusieurs types:
							</p>
							<p>
								<img src="public/img/topmount.jpg" alt="montage trucks" class="photopage"/>
							<br/>
							</p>
							<ul class="circle">
								<li><strong>le Top-mount</strong>: les trucks sont fixés directement sous la planche, la planche reste maniable.</li>
								<li><strong>le Drop-through</strong>: le plateau est découpé pour pouvoir fixer les trucks sur le plateau, la planche est donc rabaissée, elle perd en maniabilité, mais elle est plus stable. C'est un montage fréquent pour les boards de freeride.</li>
								<li><strong>les Pads</strong>: sur un montage en top-mount, il est possible d'ajouter des pads, pour surélever la planche pour la rendre encore plus maniable et éviter que les roues touchent le plateau en tournant ("wheelbite"), mais aussi de modifier l'angle des trucks avec des pads inclinés, et ainsi rendre la planche plus joueuse ou plus stable en fonction de la pratique.</li>
							</ul>
						</div>
					</li>
				</ul>
				<ul>
					<li>
						<div class="para_article">
							<h3>Les roues</h3>
							<img src="public/img/roue-longboard.png" alt="roues" class="photoarticle"/>
							<p>
								La taille des roues et leur dureté vont jouer aussi sur le comportement de la planche.
								Plus le diamêtre de la roue sera élevé, plus vous attendrez des vitesses élevées sans efforts, par contre une roue plus petite sera plus maniable. Une roue large sera plus stable et aura plus d'accroche, mais sera moins maniable qu'une roue plus fine. <br/>
								Enfin la dureté joue sur l'adhèrence et le roulage, l'indice de dureté est en Shore "A", plus le chiffre est élevé, plus la gomme est dure. Les roues tendres en 78 ou 80A ont une bonne accroche et un bon confort, donc idéale pour du carving ou du cruising, les roues plus dures 84/86 A sont des roues qui vont avoir tendance à déraper et qui vont prendre plus de vitesse, donc plus pour du freeride et de la descente.
							</p>
		
						</div>
					</li>
				</ul>
				<ul>
					<li>
						<div class="para_article">
							<h3>Les roulements</h3>
							<img src="public/img/longboard-bearings.jpg" alt="roulements" class="photoarticle"/>
							<p>
								Au niveau du type de roulement compatible, tous les roulements de type 608 (8 désignant le diamètre en millimètre de l'axe de la roue) s'adaptent.
								Autre norme, la norme ABEC, allant de 1 à 9, plus le chiffre est élevé, plus le roulement sera rapide, mais aussi plus fragile, donc optez pour un roulement ABEC 5 ou 7. Certaines marques de roulements n'utilisent pas la norme ABEC, ce n'est pas pour ça qu'ils sont moins bon (la marque Powell par exemple, qui est une des meilleures marques).
								Les roulements s'entretiennent si vous voulez les garder un peu, donc frottez la surface extèrieur du roulement avec un chiffon ou un mouchoir pour en lever la poussière et séchez les quand vous avez rouler dans l'eau. C'est le minimum que vous pouvez faire sans les démonter.
							</p>
		
						</div>
					</li>
				</ul>
				<ul>
					<li>
						<div class="para_article">
							<h3>Les diffèrentes pratiques:</h3>
							<ul>
								<li><strong>Cruising</strong>: c'est la version ballade et déplacement du longboard. On peut utiliser une petite planche très maniable pour la ville ou plutôt une longue planche avec du flex pour les grands espaces. Type de montage: trucks Topmount 50°, roues tendres (78 ou 80A).</li><br/>
								<li><strong>Carving</strong>: c'est l'enchainement de virages à vitesse moyenne, la version surf du bitume, le plateau peut être rigide pour plus de réactivité ou plus flexible pour plus de confort et pouvoir pomper. Type de montage: trucks Topmount 50°, roues tendres (78 ou 80A).</li><br/>
								<li><strong>Downhill</strong>: comme son nom l'indique, c'est de la descente, il faut un plateau rigide et bien concave, avec des trucks avec un angle faible en drop-through de préférence,pour un maximum de stabilité. Type de montage: trucks drop-through ou Top-mount 40-44°, roues intermédiare 82 A.</li><br/>
								<li><strong>Freeride</strong>: c'est un mélange de descente et de carving, où l'on recherche une autre forme de glisse, avec les slides. Les boards sont des twintips pour pouvoir rider dans les deux sens, les plateaux sont rigides et concaves pour avoir de la réactivité, les trucks sont montés en drop-through pour plus de stabilité lors des slides et les roues sont dures pour pouvoir déraper.
								Type de montage: trucks drop-through 44°, roues dures 84-86A.</li><br/>
								<li><strong>Freestyle/Dancing</strong>: le but est de réaliser des figures sur la planche en "sidewalking" ou faire des figures avec la planche comme en street. Les boards sont longues  pour pouvoir se déplacer dessus et elle dispose de nose et de tail pour pouvoir effectuer des figures.
								Type de montage: trucks top-mount 50°,roues dures.</li><br/>
								<li><strong>Street</strong>: une petite planche maniable avec un bon tail pour pouvoir faire des ollies et des roues biens dures pour faire des slides.
								Type de montage: trucks top-mount 44 ou 50°, roues dures.</li><br/>
								<li><strong>Slalom</strong>: comme en ski, le but est d'être le plus rapide sur un parcours avec des plots, donc on utilise une board courte hyper maniable. 
								Type de montage: trucks Top-mount 50° avec pads inclinés pour ouvrir l'angle sur le truck avant et le fermé sur le truck arrière, roues tendres.</li><br/>
							</ul>
