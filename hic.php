							<div class="para_article">
								<h3>Le montage d'un HIC:</h3>
						
								<img src="photos/hic2.jpg" alt="hic2" class="photoarticle"/>
								<p>
									Un HIC est composé, d'un shim, d'une étoile et d'une vis de compression.
								</p>
								<p>
									<strong>Attention!!!</strong> Il faut s'assurer avant d'essayer d'installer ce système, qu'il est bien compatible avec votre fourche et votre guidon. Votre fourche doit être, soit creuse au bout afin que l'on puisse y mettre l'étoile, soit avec un filetage pour pouvoir y visser la vis de compression (même type que fourche pour SCS), elle doit aussi ne pas être trop longue, c'est à dire qu'il doit y avoir au moins 1 cm de diffèrence entre le haut de la fourche et le haut du shim qui va venir s'appuyer sur le jeu de direction. Car sinon on ne va pas pouvoir faire de compression en serrant! 
									Pour le guidon, il doit avoir un gros diamètre "Oversized" (guidons Lucky, Raw, Apex, RAD, Madd Battwing), sinon le shim que l'on a mis sur la fourche ne rentrera pas dans le guidon.
									Si votre fourche est trop longue, il ne vous reste plus qu'à la couper à la bonne hauteur, il vous faut une bonne scie à métaux et un établi ou un étau pour tenir la fourche et la tailler bien droite.
									Ensuite, il faut enfoncer l'étoile de compression dans la fourche. Pour enfoncer l'étoile, vous avez besoin d'un marteau, d'un tournevis relativement fin dont la lame rentre dans le trou de l'étoile. Positionnez l'étoile avec les branches recourbées vers vous, positionnez le tournevis dedans et frappez bien droit jusqu'à que l'étoile soit enfoncée complètement. 
									Enfin, enfoncez le shim sur la fourche, mettez la vis dans le trou et serrez jusqu'à que ça force.
									Vous n'avez plus qu'à remettre votre guidon et serrez votre collier de serrage et le tour est joué.
								</p>
