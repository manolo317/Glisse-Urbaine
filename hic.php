					<img src="photos/hicschema.png" alt="hic schéma " class="photoarticle"/>
					<h3>Un HIC c'est quoi?</h3>
					<p>
						<strong>Handlebar Integrated Compression System</strong> ou <strong>IHC, Integrated Headset Compression</strong>soit un système de compression intégré dans la fourche. 
						Pour le HIC, c'est un <strong>"Shim"</strong>, sorte de capuchon que l'on va placer sur la fourche et qui va s'appuyer sur le jeu de direction. Ce type de compression nécessite d'avoir une fourche avec un filetage sur le dessus, ou creuse (ou couper), dans laquelle on va pouvoir placer une étoile. La fourche ne doit pas être trop longue non plus pour qu'il y ait un espace entre le shim et le haut de la fourche, afin que la compression puisse s'effectuer.
						Ce système ne se monte qu'avec des guidons "Oversized", qui ont un diamètre intérieur adapté à celui du shim, il faudra bien sur utiliser un collier de serrage de 34.9 mm. 
						Le IHC, reprend le même système, mais il est directement intégré dans la fourche, c'est le cas des fourches <strong>Blunt Declare, SOB ou CNC</strong>, ou de la <strong>Sacrifice Cyborg</strong>, d'autres marques ont repris ce système (Grit, Slamm,...). Ce système permet de monter des guidons de diamètre "standard".
					</p>
			</div>
