								<h3>Le montage d'un ICS:</h3>
						
								<p>
									Pour pouvoir monter ce système de compression, il faut avoir le bon type de fourche, c'est à dire une fourche ayant un trou assez gros pour pouvoir laisser passer la vis, mais pas trop gros non plus, sinon on ne pourra pas fixer la vis avec une rondelle (<strong>kit de compression 006</strong>).<br/>
									Ensuite, l'étoile de compression se fixe dans tous les guidons de diamètre intérieur "standard", mais avant de l'enfoncer, il faut déjà calculer de combien, pour cela il faut mesurer jusqu'où s'enfonce le guidon dans la fourche et placer l'étoile juste après.
									Pour enfoncer l'étoile, vous avez besoin d'un marteau, d'un tournevis relativement fin dont la lame rentre dans le trou de l'étoile et le manche rentre dans le guidon (<strong>image 1</strong>).<br/> Positionnez l'étoile avec les branches recourbées vers vous (<strong>image 2</strong>), positionnez le tournevis dedans et frappez bien droit jusqu'à que l'étoile soit assez enfoncée (<strong>image 3 et 4</strong>).<br/>
									Remontez votre guidon dans la fourche, et visez la vis dans le trou de votre fourche jusqu'à que cela force et qu'il n'y ait plus de jeu (<strong>image 5 et 6</strong>). Si cela ne force pas, c'est que votre étoile est foutue et qu'elle tourne dans le guidon, donc changez d'étoile! (une étoile est difficile à enlever, il faut l'enfoncer et en remettre une derrière).
								</p>
								
