					<h3>Un ICS c'est quoi?</h3>
					<p>
						<strong>Inverted Compression System</strong>, soit littéralement en français: système de compression inversé.<br/>
						Concrètement, cela correspond à une vis que l'on va visser à l'intérieur de la fourche et qui va venir se bloquer dans une étoile de compression, qui va servir d'écrou, que l'on aura fixé dans le guidon.<br/>
						Cela va permettre de compresser la fourche et le guidon contre le deck et d'enlever tout le jeu qui pourrait y avoir. <br/>
						Ce système n'est pas le plus efficace, cela écrase le jeu de direction et peut empêcher de bien tourner, la vis se desserre au bout d'un moment et le jeu revient, donc il faut resserrer de temps en temps. 
						Cela reste le système le moins cher et le plus léger.
					</p>
			</div>
