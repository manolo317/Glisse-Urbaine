				<ul>
					<li>
						<div class="para_article">
							<h3>Le guidon (ou bar):</h3>
					
							<p>
								<img src="photos/guidon.jpg" alt="guidon" class="photoarticle"/>
								Les guidons sont soit en <strong>aluminium</strong>, soit en <strong>acier</strong> (4130 Chromoly pour être précis). En aluminium, ils sont très légers (600 à 800 g), contre plus d'un kilo pour les guidons aciers, mais qui sont beaucoup plus résistants.<br/>
								Il existe deux formes de guidon le <strong>Y bar</strong> et le <strong>T bar</strong>, en référence à leurs formes, ils peuvent aussi avoir un "back sweep" (manches du guidon recourbés vers l'arrière), qui facilitent la prise en main et les figures en bar spin. La plupart des guidons ont des fentes au niveau du bas du guidon, afin de bien pouvoir les serrer à l'aide du collier de serrage contre la fourche.<br/>
								En terme d'adaptabilité, les guidons s'adaptent sur toute les fourches ou decks, la seule chose à laquelle il faut faire attention, c'est le <strong>diamètre</strong> du guidon. Il faut prendre en compte, le diamètre extérieur du guidon, mais aussi l'intérieur. Il existe deux diamètres extérieurs <strong>31,8 mm</strong> pour les guidons en acier de diamètre intérieur "standard" et <strong>34,9 mm</strong> pour ceux en aluminium et ceux en acier de diamètre intérieur "Oversized"(District Oversized, Blazer Y bar, Flavor, Raw, Lucky, Apex...) et tous les <strong>colliers de serrage</strong> ne sont pas adaptés pour le plus gros diamètre (certains colliers possèdent un spacer ou adaptateur qui permet de s'adapter sur les deux tailles). Pour les deux diamètres intérieurs: "standard" et "Oversized"; il faudra faire attention au système de compression, pour qu'il s'adapte.
							</p>
							<br/>
							<iframe class="video-large" width="854" height="480" src="https://www.youtube.com/embed/ZPfvIdtMNrM" frameborder="0" allowfullscreen></iframe>
						</div>
					</li>
					<li>
						<div class="para_article">
							<h3>La fourche (ou fork):</h3>
					
							<p>
								<img src="photos/fourchedistrict.jpg" alt="fourche" class="photoarticle"/>
								La fourche est la <strong>liaison</strong> entre la roue avant et le guidon, elle esr faite en aluminium ou en acier et pèse entre 200 et 400 g. Elle est composée d'un <strong>pivot</strong>: le tube qui rentre dans la potence du deck; et de <strong>fourreaux</strong> qui vont permettre de fixer la roue.<br/> 
								En plus d'être l'élément qui soutient le <strong>deck</strong> et le <strong>guidon</strong> de votre trottinette, elle permet de gèrer la <strong>direction</strong> avec l'aide du <strong>jeu de direction</strong>.<br/>
								A noter aussi, que les fourreaux ont un léger décalage en avant de l'axe de direction (<strong>offset</strong> en anglais), donc attention lors des montages à bien mettre la fourche à l'endroit.<br/>
								Autre fonction importante de la fourche, qui va déterminer le type de fourche, c'est la <strong>compression</strong>.
								En effet, la plupart des fourches possèdent un <strong>système de compression</strong> ou nécessitent de mettre un certain système de compression.
							</p>
							<br/>
							<iframe class="video-large" width="854" height="480" src="https://www.youtube.com/embed/XYWgZ8CdZIo" frameborder="0" allowfullscreen></iframe>
							</p>
						</div>
					</li>
					<li>
						<div class="para_article">
							<h3>Le Deck (ou plateau):</h3>
							<p>
								<img src="photos/deck%20aos.jpg" alt="deck" class="photoarticle"/>
								Le <strong>deck</strong> est la partie centrale de la trottinette, il est composé d'un <strong>plateau</strong> et d'une <strong>potence</strong>, où la <strong>fourche</strong> va se glisser. Ils sont forgés en aluminium et pèsent entre 1 et 1,8 kg, ils sont généralement en une seule pièce (sauf pour le deck Lucky Evo2, ce qui ne le rend pas moins solide).<br/>
								La <strong>longueur</strong>, la <strong>largeur</strong>, ainsi que le <strong>poids</strong> du deck influent sur la façon de rider. Donc un deck large et lourd conviendra aux gros gabarits qui envoient des gros sauts.<br/>
								La qualité du deck, et ce qui explique aussi les différences de prix, est due à la qualité, de <strong>l'alliage</strong>, de <strong>l'usinage</strong> et des <strong>soudures entre la potence et le plateau</strong> (zone où ça casse!).
								Attention aussi à la <strong>longueur de la potence</strong>, avec une potence longue (comme les District notamment), les fourches courtes ne dépassent pas assez pour assurer la liaison avec le guidon (sauf en mettant un SCS).
								Autre chose à voir au niveau de la potence, qui va déterminer le choix du jeu de direction, s'il y a des <strong>cuvettes</strong> de chaque côté de la potence, il vous faut un <strong>jeu de direction intégré</strong>, sinon il vous faut un <strong>semi-intégré</strong>.
							</p>
						</div>
					</li>
					<li>
						<div class="para_article">
							<h3>Le jeu de direction (ou headset):</h3>
					
							<p>
								<img src="photos/jdd.jpg" alt="jdd" class="photoarticle"/>
								Le <strong>jeu de direction</strong> est la pièce qui va permettre au <strong>guidon</strong> et à la <strong>fourche</strong> de tourner parfaitement, il est composé de de deux <strong>roulements</strong> huilés ou graissés, situés dans des <strong>cuvettes</strong> au dessus et en dessous de la potence du deck.<br/>
								C'est une pièce assez fragile, qui est soumise à beaucoup de pression, et c'est souvent ce qui casse en premier quand la trottinette a trop de jeu.<br/>
								Il existe deux types de jeu de direction, qui sont dépendant du type de deck que vous avez, les <strong>intégrés</strong> et les <strong>semi-intégrés</strong>. Les <strong>intégrés</strong> sont composés seulement de roulements qui vont se placer dans les cuvettes prévues à cet effet dans votre deck, ainsi que de deux <strong>rondelles</strong> qui vont faire le joint de part et d'autre de la fourche et un couvercle, pour empêcher de mettre des poussières dans le roulement. Les <strong>semi-intégrés</strong> possèdent juste 2 <strong>cages</strong> que l'on va enfoncer de part et d'autre de la potence, quand celle-ci n'est pas pourvue de <strong>cuvettes</strong>.
							</p>
							<br/>
							<iframe class="video-large" width="854" height="480" src="https://www.youtube.com/embed/uDPGQhaI4gA" frameborder="0" allowfullscreen></iframe>
						</div>
					</li>
					<li>
						<div class="para_article">
							<h3>Le collier de serrage:</h3>
					
							<p>
								<img src="photos/collier%20de%20serrage.jpg" alt="collier de serrage" class="photoarticle"/>
								Le <strong>collier de serrage</strong> est la pièce qui permet de <strong>serrer le guidon sur la fourche</strong>, et de le fixer pour ne pas qu'il bouge.<br/>
								Ils sont en aluminium ou en acier, et leur taille va du simple au quintuple, qui dépend du nombre de vis. Plus il est grand, et plus le guidon sera mieux serrer et ne se desserrera pas trop vite. <strong>Mais attention!!!, un collier de serrage ne remplace pas un système de compression (sauf le SCS qui fait les deux!)</strong>, les vibrations dues au sauts vont forcement desserrées les vis du collier de serrage, donc <strong>resserrez les vis régulièrement et mettez un système de compression sur votre trottinette!</strong><br/>
								Le seul piège pour le choix du collier de serrage c'est son <strong>diamètre</strong>, qui dèpend de celui de votre guidon, 31,8 mm ou 34,9 mm. Certains colliers s'adaptent sur tous les guidons car il possèdent un <strong>spacer</strong> (adaptateur en alu) qui permet de faire les deux diamètres.
							</p>
							<br/>
							<iframe class="video-large" width="854" height="480" src="https://www.youtube.com/embed/e7SBKcpWzEI" frameborder="0" allowfullscreen></iframe>
						</div>
					</li>
					<li>
						<div class="para_article">
							<h3>Le frein:</h3>
					
							<p>
								<img src="photos/frein.jpg" alt="frein" class="photoarticle"/>
								Le frein est la lame qui vient se fixer au dessus de la roue arrière, il en existe deux types:
								le frein fixe et le frein à ressort.<br/>
								Le frein fixe est le plus utiliser et le plus fiable.<br/>
								<strong>Attention!!! Seul un frein de la marque de votre deck sera compatible, il n'y a aucune correspondance de modèle de frein entres les marques.</strong>
							</p>
							<br/>
							<iframe class="video-large" width="854" height="480" src="https://www.youtube.com/embed/7HLen21IlHM" frameborder="0" allowfullscreen></iframe>
