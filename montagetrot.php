				<h3>Ce qu'il faut savoir pour bien choisir les différents éléments:</h3>
				<p>
					Si vous êtes un papa qui passe tous ces week-ends à essayer de réparer la trot du petit, ou que vous êtes un jeune trottirider perdu entre du jeu et un SCS, cette rubrique est faite pour vous.<br/>
					Tout d'abord, nous allons voir l'anatomie d'une trottinette pour pouvoir comprendre le rôle de chaque élément et comment ils s'assemblent.<br/>
					Car bricoler une trottinette n'est pas un jeu d'enfant!<br/> <strong>Toutes les pièces, marques et systêmes ne s'adaptent pas...</strong><br/>Il faut souvent sortir la scie, le marteau et l'huile de coude. <br/>
					<strong>Donc pour éviter quelques après-midi prise de tête, étudiez attentivement ce dossier pour pour pouvoir choisir le montage adéquate.</strong><br/>
