							<h3>Avertissements!!!</h3>
					
							<p>
								<img src="photos/poubelle.jpg" alt="poubelle" class="photoarticle"/>
								La trottinette coûte chere, très chere! Et la durée de vie des pièces est assez limitée (moins d'un an), surtout pour le trottinettes premiers prix. Les trottinettes sont soumises à de gros chocs répétés, et la mécanique ne permet pas de bien amortir ces chocs. La qualité et solidité des pièces est très variables, et des pièces comme les jeux de directions sont très fragiles, donc il ne faut pas s'étonner de revenir souvent au shop...
								Il est encore temps de se mettre au roller ou au skate... ou sinon il va falloir sacrèment déchirer à l'école!
							</p>
