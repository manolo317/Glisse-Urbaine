					</li>
					<li>
						<div class="para_article">
							<h3>Le grip:</h3>
					
							<p>
								<img src="photos/grip.jpg" alt="grip" class="photoarticle"/>
								Le <strong>grip</strong> est une sorte de papier de verre qui se colle sur la surface du deck pour permettre une bonne <strong>adhèrence</strong> des pieds.<br/> 
								Ce sont les mêmes qui sont utilisés pour le skate, plus le grain est épais, plus il accrochera, mais plus il abimera les chaussures aussi.<br/>
								Pour changer le grip il faut faire attention de bien nettoyer la surface du deck (et donc enlevé l'ancien grip) pour que le nouveau grip adhère bien sur le deck.
							</p>
							<br/>
						</div>
					</li>
					<li>
						<div class="para_article">
							<h3>Les pegs:</h3>
					
							<p>
								<img src="photos/pegs.jpg" alt="pegs" class="photoarticle"/>
								Les <strong>pegs</strong> sont des petits tubes en acier ou en aluminium, qui se fixent sur les <strong>axes des roues</strong>, qui permettent de <strong>se caler</strong> sur des modules ou de <strong>slider</strong>.<br/>
								La plupart des pegs sont compatibles avec tous les decks, attention à la forme du peg qui peut ne pas s'emboiter au niveau de l'axe arrière du deck.<br/>
								Le principal problème, et pas le moindre, c'est la <strong>longueur de l'axe arrière du deck</strong>, en effet, pour pouvoir mettre le peg il faut que l'axe soit un peu plus grand. 
								Et malheureusement, une grande partie des pegs sont vendus sans axes ou avec un axe trop court pour pouvoir se fixer à l'arrière.<br/> 
								Certains pegs, comme les <strong>Blunt</strong> ou les <strong>Tilt</strong>, vous permettront de poser vos pegs sur presque tous les decks (excepté les decks <strong>District</strong>, trop larges). Sinon, acheter des axes (de préférence en acier!), en mesurant bien avant, et des clés à pipe de 13 qui rentre à l'intérieur des pegs.
							</p>
						</div>
					</li>
					<li>
						<div class="para_article">
							<h3>Les roues:</h3>
					
							<p>
								<img src="photos/roue.jpg" alt="roue" class="photoarticle"/>
								Les <strong>roues</strong> de trottinette ont la même composition que les roues de roller, elles sont <strong>polyuréthane</strong>, avec un noyau en <strong>plastique</strong> ou en <strong>aluminium ("metal-core")</strong>. Les roues metal-core sont les plus solide et sont essentielles pour supporter de grosses réceptions, les roues en plastiques équipent seulement les trottinettes premiers prix.<br/>
								La dureté des roues est souvent de <strong>88 A (échelle de dureté, en shore)</strong>, mais pour une même dureté, la <strong>qualité de la gomme</strong> change en fonction des marques et souvent du prix de la roue.<br/>
								Il existe plusieurs tailles de roue, <strong>100 mm</strong>, <strong>110 mm</strong> et même <strong>120 mm</strong> et <strong>125 mm</strong>! Les roues de 100 mm s'adaptent sur toutes les trottinettes, ce n'est pas vrai pour les 110 mm et plus(donc à vérifier avant d'acheter des roue).
								Les roues de 110 mm, et encore plus avec les roues de 120 et 125 mm, permettent un plus grand <strong>confort</strong> et une plus grande <strong>vitesse</strong>, les 100 mm permettent une plus grande <strong>stabilité</strong> et <strong>maniabilité</strong>, et pardonne un peu plus les réceptions.
							<br/><br/>
							<iframe class="video-large" width="854" height="480" src="https://www.youtube.com/embed/POEQ1Jvl80Q" frameborder="0" allowfullscreen></iframe>
							</p>
						</div>
					</li>
					<li>
						<div class="para_article">
							<h3>Les roulements (ou bearings):</h3>
					
							<p>
								<img src="photos/roulements.jpg" alt="roulements" class="photoarticle"/>
								Le <strong>roulement</strong> est la petite pièce circulaire qui va permettre de faire tourner les roues autour de son axe. Il faut <strong>deux roulements</strong> pour chaque roue, qui vont venir se mettre dans part et d'autre de la roue. <strong>Attention!!!</strong>, pièce essentielle, que l'on ne pense pas à mettre, <strong>l'entretoise</strong>, petit cylindre en aluminium qui va se mettre entre les deux roulements, et qui va permettre d'assurer la <strong>solidité et le bon roulage de la roue</strong>.<br/>
								Au niveau du type de roulement compatible, tous les roulements de <strong>type 608</strong> (8 désignant le diamètre en millimètre de l'axe de la roue) s'adaptent.<br/> Autre norme, la <strong>norme ABEC</strong>, allant de 1 à 11, plus le chiffre est élevé, plus le roulement sera <strong>rapide</strong>, mais aussi plus <strong>fragile</strong>, donc optez pour un roulement ABEC 5 ou 7. Certaines marques de roulements n'utilisent pas la norme ABEC, ce n'est pas pour ça qu'ils sont moins bon (la marque <strong>Powell</strong> par exemple, qui est une des meilleures marques).<br/>
								Les roulements <strong>s'entretiennent</strong> si vous voulez les garder un peu, donc frottez la surface extérieur du roulement avec un chiffon ou un mouchoir pour en lever la poussière et séchez les quand vous avez rouler dans l'eau. C'est le minimum que vous pouvez faire sans les démonter.<br/>
								Il est très difficile de mettre ou d'enlever des roulements sur des roues metal-core. Il vous faut les enfoncer à l'aide d'un marteau et d'une cale pour pas trop abimer les roulements, mettez aussi un peu de graisse autour du roulement pour l'enfoncer plus facilement. Pour les enlever, c'est encore plus compliqué, il vous faut une presse pour caler la roue, et taper au niveau de l'entretoise avec un gros tournevis et un marteau pour faire sauter le roulement. C'est une opération qui nécessite les bons outils et qui <strong>abiment la roue et les roulements</strong>. 
							</p>
