				<p>
					Les 24 heures du Mans sont maintenant depuis 13 ans un passage obligé pour les amateurs de Roller. 
					Créées en 2000 par l'association "La tribu Rollers", les 24 h sont un grand un grand succès populaire, surfant sur le développement des randonnées urbaines, le nombres de participants n'a cessé de croitre, passant de 600 à 1600 (en comptant les randonneurs de la parade.</br>
					L'organisation a su s'adapter à cette expansion, et a proposé un évènement regroupant tous les amoureux de roulettes.</br>
					Parce qu'avant tout ca reste une compétition, très disputée (même si c'est toujours Powerslide qui gagne à la fin!), où s'affronte les meilleurs coureurs de la planète, mais aussi le rouleur du dimanche, c'est ce qui fait tout le charme de cette course.</br>
					Le cadre aussi est magnifique, avec le circuit Bugatti, et la célèbre montée Dunlop qui casse les pattes, long de 4,185kmn il offre un cadre parfait pour se dépasser.
				</p>
			
