							Un peu d'histoire: les <strong>cruisers</strong> sont les ancêtres du skate moderne, ce sont des répliques des skates des années 70. 
							Le skateboard est inventé dans les années 30, il est inspiré par la <strong>trottinette</strong> (à méditer pour les skaters intolérants...) à laquelle on a enlevé le guidon. 
							<br/>
							Il refait surface dans les années 50, utilisés par les surfeurs pour se déplacer et pour "surfer sur Terre" quand les conditions en mer étaient mauvaises.<br/> Mais il devient vraiment populaire dans les années 70 avec l'invention de la roue en <strong>polyuréthane</strong>, il se développe en Europe et s'impose comme une pratique sportive à part entière. Les marques fleurissent, le matériel devient de plus en plus performant et permet l'invention de la plupart des figures que l'on connait aujourd'hui.
							<br/>
						</p>
