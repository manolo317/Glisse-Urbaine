				<h3>Un cruiser c'est quoi?</h3>
				<p>
					Un <strong>cruiser</strong>, c'est une petite planche de skateboard, avec un plateau en plastique ou en bois souple, avec des petits trucks (100 à 139 mm de large),  et des roues tendres de préférence (78 A). Donc une petite planche très maniable et très agréable à rouler due, à son petit gabarie, son plateau souple et ses roues tendres. Elle est plutôt faite pour se déplacer, un peu moins pour faire des figures, surtout les cruisers en plastiques, qui n'ont pas de concave et pas de grips, donc les ollies sont plus complexes.
				</p>
