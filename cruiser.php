						<h3>Le marché du Cruiser:</h3>
				
						<p>
							En France, c'est le shop <a href="http://www.hawaiisurf.com/" title="vers le site Hawaii Surf">Hawaii Surf</a>, qui a senti le revival, ils ont resortis les célèbres planches <strong>Banzai</strong>, la marque française créée par <strong>Jean-Pierre Marquant</strong> en 1975.<br/> Il y a la Banzai Black, avec laquelle il a battu le record du monde de vitesse (108 km/h!), avec son plateau en plastique et son kicktail pour passer les trottoirs en toute facilité et la Banzai fibre.
						</p>
							
						<p>	
							<img src="public/img/penny2.jpg" alt="penny" class="photoarticle"/>
							Les autres marques qui ce sont emparées de cette mode sont tout d'abord, <strong>Penny Skateboard</strong>, marque australienne, créée en 1983, par <strong>Ben Mackay</strong>. C'est une marque vraiment spécialisée dans le cruiser plastique, et elle ne fait exclusivement que ça, et elle le fait plutôt bien. <br/>Ben Mackay a voulu retrouver les sensations qu'il a eut en apprenant le skate dans sa prime jeunesse, et il nous offre des cruisers de qualité, notamment au niveau des trucks et des roues qui sont bien au dessus de ses concurrents, avec ses <strong>Penny Nickel</strong> 22 et 27 pouces.
						</p>
