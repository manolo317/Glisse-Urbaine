								D'ailleurs, Matthias Knoll a déjà annoncé qu'ils allaient se mettre au <strong>Roller Derby</strong>, avec sa marque <a href="http://www.chaya-brand.com/">Chaya</a> le dernier secteur de marché à aller chercher et surtout un des secteurs le plus dynamique.
								Powerslide continue sa marche en avant, et affiche des chiffres d'affaires en croissance d'année en année (16 millions d'euros en 2012) dans un marché du Roller qui souffre, encore une habitude allemande de s'en sortir mieux que ses voisins! 
