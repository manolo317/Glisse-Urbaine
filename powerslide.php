						<div class="para_article">
							<h3>Historique de Powerslide:</h3>
					
							<p>
								C'est <strong>Matthias Knoll</strong>, un jeune passionné de roller de vitesse allemand, qui décide en 1988, à 18 ans, de se lancer dans la vente de matériel de course, pour financer ses compétitions.
								</br>En 1994, il monte la société <a href="http://www.powerslide.de/" title="vers le site de Powerslide"/>Powerslide</a> avec son associé Stefan Göhl, il se spécialise dans la distribution de marque de roller agressif américaine en Europe, marché en plein essor à cette période.</br> Après une période faste, en 1997, le marché du roller, et spécialement celui de l'agressif, s'écroule, mais Matthias Knoll en profite pour investir en créant la marque <strong>USD</strong>, qui est encore numéro une aujourd'hui.</br>
								Powerslide a ensuite lancé sa propre ligne de patins, avec des rollers de course en 2003, puis suivront, les rollers nordiques, de randonnées et de freeskate, gagnant chaque année en qualité et en crédibilité.
							</p>
						</div>
					</li>
					<li>
						<div class="para_article">
							<h3>L'empire du roller agressif:</h3>
					
							<img src="public/img/usd_carbon_free.jpg" alt="USD Carbon Free Richie Eisler" class="photoarticle"/>
							<p>
								C'est déjà dans le milieu du roller agressif que Powerslide s'est imposé avec <strong>USD</strong>, qu'il a installé comme la marque référence, puis en rachetant en 2004, le roller <strong>Xsjado</strong> à <strong>Salomon</strong>.
								Powerslide créé ensuite <a href="http://theconference.org/" title="lien vers The Conference"/>La Conférence</a>, qui regroupe les marques USD, Xsjado, Kizer et Undercover, un groupe qui permet de continuer à développer le milieu du Street en apportant des innovations d'autres secteurs du roller, comme les boots en carbone ou les platines de Powerblading (platines de street pouvant accueillir des roues 76 ou 80mm)par exemple.
								Powerslide avec la Conférence règne sans partage sur le roller agressif.
							</p>
							<br><br>
						</div>
					</li>
					<li>
						<div class="para_article">
							<h3>Les patins de course:</h3>
					
							<img src="public/img/core-icon.jpg" alt="Core Icon" class="photoarticle"/>
							<p>
								Matthias Knoll a toujours été un grand amateur de vitesse, qu'il pratique en compétition encore aujourd'hui, et il n'a cessé d'investir dans le développement des patins de vitesse.
								Powerslide a commencé par sortir des patins de milieu de gamme, avec le R2 ou le R4, des patins abordables et d'un autre côté, il développe des patins haut de gamme, à la pointe de la technologie, avec la marque <strong>Core</strong>. 
								Powerslide supporte les meilleurs coureurs de la planète, avec son équipe <strong>Powerslide Racing</strong>, comme la star belge <strong>Bart Swings</strong>.
								Il possède aussi la marque de roues américaine <strong>Matter</strong>, leader dans le marché de la vitesse.
							</p>
								
						</div>
					</li>
					<li>
						<div class="para_article">
							<h3>Le roller tout-terrain:</h3>
					
							<img src="public/img/powerslide-vi-suv.jpg" alt="Powerslide Virus SUV" class="photoarticle"/>
							<p>
								En 2007, ils se mettent même à faire des patins nordiques, un secteur niche dans le milieu du roller, mais Powerslide ne fait pas les choses à moitié et propose plusieurs modèles de qualité.
								On retrouve une recette de la réussite de Powerslide, c'est l'utilisation de pièces d'autres patins dans diffèrentes familles. Dans la gamme Nordique, on retrouve la coque des Xsjado avec XC Skeleton, celle des Phuzion avec XC Trainer, XC Path et XC Trail, et plus récemment celle des Virus, avec le <a href="#" title="promo Virus SUV par Adrien Anne">Vi SUV</a> et le Vi Shockliner.</p>
							</p>
							<br>	
						</div>
					</li>
					<li>
						<div class="para_article">
							<h3>La gamme Virus:</h3>
					
							<img src="public/img/virus.jpg" alt="Powerslide VI 100" class="photoarticle"/>
							<p>
								Dernière innovation en date, avec la gamme <strong>Virus</strong>, une coque très légère, avec un système de serrage révolutionnaire (le Atop 2) et une design original. Si le Virus a quelques défauts de solidité, il reste quand même assez intéressant du point de vue performance et confort.
								Powerslide a utilisé cette nouvelle chaussure dans presque toutes ses gammes: fitness, freeskate, vitness ou Nordic.
								On ne peut pas reprocher à Powerslide de prendre des risques, quant on voit que Rollerblade ou Fila sortent les mêmes patins depuis 10 ans.
							</p>
						</div>
					</li>
					<li>
						<div class="para_article">
							<h3>Doop et Ennui:</h3>
					
							<img src="public/img/Doopskates.jpg" alt="Doop" class="photoarticle"/>
							<p>
								Cette année, Powerslide a lancé avec <a href="powerblading.php" title="lien vers dossier Powerblading"/>Oli Bennet</a>, la marque <strong>Doop</strong>, des patins avec le concept du <a href="http://vimeo.com/53597324" title="video Doop instruction"><strong>Walk and ride</strong></a>, en reprenant la coque du Xsjado. Encore une fois Powerslide recycle ses boots, mais le concept est intéressant et le créneau occupé par <a href="http://localhost/glisseurbaine/V4/details?id=5"><strong>Hypno</strong></a> depuis des années mérite d'être mieux exploité. Le fait d'utiliser ses propres chaussures et que le patin soit réglable sur plusieurs tailles sont de gros points forts. Le maintien est correct, à condition d'utiliser des chaussures adaptées, les platines, roues et roulements sont aussi de bonnes factures, au final, malgré la souplesse de la chaussure, le Doop est assez vif et maniable .Le Doop est décliné en 4 modèles, deux fitness (Classic Blue et Red) avec des roues de 84 mm, un freeskate (Freestyle) avec des roues de 80 mm et le Swift avec 3 roues de 100 mm, il y en a donc pour tous les goûts. 
								Deuxième marque lancée cette année, c'est <strong>Ennui</strong>, une marque de protection haut de gamme, des crashpads, gants, protège-poignets ou encore des chevillières, en attendant une gamme plus large. Encore une fois, Powerslide a mis l'accent sur la recherche et l'innovation, les produits sont bien pensées et conçues, les matériaux ont aussi été soigné avec l'utilisation de cuir, kevlar, néoprène ou gel à mémoire de forme.
							</p>
