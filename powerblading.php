							<h3>Développement et évolution du Powerblading:</h3>
					
							<p>
								Aujourd'hui, de plus en plus d'anciens riders sont attirés par le Powerblading (comme moi :)), une pratique qui correspond plus à leur vision du roller actuel, et leur besoin de rouler librement.
								Un petite communauté s'est créée, touours sous l'impulsion de <strong>Oli Benet</strong> et sa bande à Barcelone, en 2011, il organise avec son ami <strong>Alex Rahali</strong>, une tournée dans le monde entier, permettant à tous les riders de se retrouver pour rouler ensemble. <strong>Wax&Bombing</strong> était née, avec le soutient de quelques marques, la tournée a connu sa deuxième saison et a permis d'accroitre la visibilité du Powerblading.
							</p>
