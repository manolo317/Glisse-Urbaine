						<div class="para_article">
							<h3>Le matériel:</h3>
					
							<p>
								Pour faire du <strong>Powerblading</strong>, en théorie, il faut un boot de street et une platine pouvant accueillir des roues de 80 ou 76 mm. Certains anciens ont déjà expérimentés sans le savoir, en montant les premières platines <strong>Salomon</strong> UFS sur leurs vieilles boot de street, le problème, c'est que les platines en aluminium ne sont pas trop adaptées aux slides.
							</p>
							<br/>
							<p>
								<img src="photos/CarbonIIIPB.jpg" alt="USD Carbon 3 PB" class="photoarticle"/>
								Sous l'impulsion de <strong>Oli Benet</strong>, c'est <strong>Powerslide</strong> qui c'est investi dans le <strong>Powerblading</strong>, avec ces marques <strong>USD</strong>, <strong>Kizer</strong> et <strong>Undercover</strong>.
								<br/>
								<br/>
								Kizer a d'abord sorti les <strong>Kizer Arrow</strong>, platine en aluminium pouvant accueillir des roues de 80 mm, mais avec des renforts en plastiques pour permettre une bonne glisse et éviter l'usure de la platine.
								Ensuite, ils ont sorti la <strong>Advance Pb</strong>, encore une platine pouvant accueillir 4 roues de 80 mm, mais cette fois ci complètement en composite et se rapprochant un peu plus des platines de street.
								Enfin, dernièrement, la <a href="http://vimeo.com/54594702"><strong>Level 2</strong></a>, toujours en composite, accueillant cette fois ci des roues de 76 mm maximum, mais avec un groove qui facilite le calage en Royale.
								<br/>
								<br/>
								<strong>Undercover</strong> a développé une gamme de roues de Powerblading, des roues de 76 et 80 mm d'une dureté de 86 à 94 A, permettant de ne pas accrocher en slide.
								<br/>
								<br/>
								<strong>USD</strong> a sorti les premiers roller destinés à la pratique du Powerblading, avec les <strong>Carbon 3 Pb</strong>, comprenant la boot <strong>Carbon 3</strong>, les platines <strong>Kizer Arrow</strong> et les <strong>Undercover</strong> en 80 mm et 88A, le bébé de Oli Benet!
								<br/>
							</p>
							
						</div>
						<div class="para_article">
							<img src="photos/sebafra.jpg" alt="Seba FR Agressif custom" class="photoarticle"/>
							<p>
								Il y a aussi <strong>Seba</strong> qui c'est lancé dans l'aventure avec son <a href="video-seba-fra-nicolas-colliaux.php"><strong>FR Agressif</strong></a>, qui est un FR1 auquel on a fixé des soulplates et des grindplates au niveau de la platine pour permettre de slider, pas aussi efficace qu'une boot de street, mais un bon compromis pour les freeskaters qui veulent taper quelques tricks. 
								Seba travaille aussi sur un patin de street, avec le rider australien <strong>CJ Wellsmore</strong>, qui est sorti en 2013, et qui permet la pratique du Powerblading.
							</p>
