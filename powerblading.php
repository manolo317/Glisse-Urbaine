						<div class="para_article">
							<h3>L'histoire du Powerblading:</h3>
					
							<p>
								Concrêtement, le <strong>Powerblading</strong> c'est du roller agressif, avec des grandes roues en <strong>Flat</strong> (4 roues de 80 ou 76 mm en ligne), c'est la volonté de retrouver le plaisir de rouler, de rouler vite, de pouvoir se déplacer vite en ville, sans être gêné par des petites roues et des antirockers qui ne roulent pas.
								De ce côté, on se rapproche du <strong>Freeskating</strong>, mais on garde l'esprit <strong>Street</strong>, et un matériel qui nous permet de continuer à faire tout ce que l'on peut faire avec des patins de street, enfin du moins on peut essayer...car avec 4 roues de 80 mm, il n'y a plus de <strong>groove</strong> pour pouvoir se caler, donc on est obliger de glisser sur les roues qui ont intêrét à être bien dures.
							</p>
						</div>
						<div class="para_article">
							<img src="photos/oli-benet-inercia.jpg" alt="Oli Benet Pub Inercia" class="photoarticle"/>
							<p>
								A l'origine du <strong>Powerblading</strong>, nous avons <strong>Oli Benet</strong>, rider pro américain de 35 ans, qui après avoir rouler pour <strong>Razors</strong> et <strong>Deshi</strong>, s'est investi dans l'industrie et le développement du Roller avec <a href="http://theconference.org/">The Conference</a> (marques <strong>USD</strong>, <strong>Xsjado</strong>, <strong>Kizer</strong> ou <strong>Undercover</strong> propriété de <strong>Powerslide</strong>).
								<br/><br/>
								Il y a 6 ans, il s'est installé à Barcelone, où il a monté la <a href="http://powerhouse.theconference.org/">Powerhouse</a> pour Powerslide, et où il testait des produits pour les différentes marques pour qui il travaille. Il s'est alors monté une boot Xsjado avec une platine FSK avec des roues de 80 mm, pour se déplacer en ville plus facilement de spot en spot, il a directement était happer par cette sensation de liberté et de vitesse, qu'il avait perdu durant une dizaine d'années de roulage en petites roues de street. 
								<br/><br/>
								Lorsque ces amis <strong>Richie Eisler</strong> et <strong>Dustin Werbeski</strong> viennent le rejoindre pour un petit trip à Barcelone, il leur fait découvrir la ville avec ce montage, et eux aussi accrochent tout de suite. Le petit groupe d'amis se rend compte aussi du potentiel exceptionnel de ce type de montage, cette nouvelle vitesse retrouvée, ils peuvent aborder les spots diffèremment, mais surtout atteindre des spots qu'il ne pouvaient pas imaginer avant.
								<br/><br/>
								Après un été à explorer cette nouvelle forme de roller, ils décident de créer le mouvement du <strong>Powerblading</strong>, en Octobre 2010. S'en suivent la création de l'organisation <a href="http://powerblading.org/"><strong>Powerblading</strong></a> et la création de platine pour <strong>Kizer</strong> et de roues pour <strong>Undercover</strong>.
							</p>
							<br/>
