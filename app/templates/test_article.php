<h2 class="titrePrincipalSkate">Le skateboard aux JO !</h2>
			
			<div id="introskate">
				<img src="public/img/olympic.jpg" alt="skate olympic games" class="photoarticle"/>
				<h3>Vraie bonne nouvelle?</h3>
				<p>
					A l’occasion des <strong>Jeux Olympiques de Tokyo en 2020</strong>, le skateboard fera son entrée dans la reine des compétitions. Consécration pour les skaters, reconnaissance d’une pratique sportive de plus en plus populaire, coup de com’ pour le CIO, il y a surement un peu de tout cela. En tout cas, la décision divise le monde du skateboard.
				</p>
			</div>
			
			<div id="corps">
			<ul>
				<li>
					<div class="para_article">
						<h3>Une entrée programmée :</h3>
				
						<p>
							Le <strong>Comité international olympique (CIO)</strong> a validé à l’unanimité la présence du skateboard, ainsi que  du surf, de l’escalade, du karaté, et du baseball/softball, pour l’instant, uniquement pour les <strong>JO de Tokyo</strong>, mais ils valideront leur ticket si la compétition se passe bien. Pour les critères de sélection, le CIO, avance la popularité de la pratique chez les jeunes, ainsi que la mixité de ses pratiquants.<br>
							Il est clair que le skateboard, et les sports de glisse urbains en général, ont le vent en poupe, plus que la pratique, le skateboard est devenu un phénomène culturel, le symbole du cool même, on ne compte plus les publicités où on voit un skate.
							Et les JO veulent clairement profiter de cette image « cool », pour rajeunir les spectateurs et attirer de nouveaux partenaires (et oui les JO c’est du business avant tout !).<br>
							La <a href="http://www.rollersports.org/" title="lien vers la FIRS">FIRS</a> (Fédération Internationale de Roller Sports), qui gère la pratique fédérale du skateboard au niveau mondial, se félicite de cette promotion, mais l’entrée aux JO fait grincer des dents dans un milieu où la pratique encadrée et les compétions ne font pas l’unanimité.
						</p>
					</div>
				</li>
				<li>
					<div class="para_article">
						<h3>Les épreuves en question:</h3>
				
						<p>
							<img src="public/img/skateboarding-olympics.jpg" alt="skatepark olympique" class="photoarticle" title="le skatepark olympique"/>
							<br>
						</p>
						<p>
							Les épreuves prévues aux prochains JO, sont le Bowl et le Street, épreuves classiques que l’ont retrouve dans toutes les grandes compétitions (X-Games, Street League, FISE, …), avec bien sûr des épreuves masculines et féminines. On a hâte de voir les sélections de skaters et surtout les critères de sélections !
							<br>
							<br>
						</p>
					</div>
				</li>
				<li>
					<div class="para_article">
						<h3>Un sport divisé en deux:</h3>
				
						<p>
							Le monde du skateboard a toujours été divisé, avec d’un côté les fervents d’une pratique libre, sans règles, sans contraintes, sans organisation, sans compétitions, avec un esprit bien rebel et anticonformiste.
							Et de l’autre, ceux qui veulent que le skate soit reconnu par le monde sportif, qu’il se professionnalise, se structure, que des grandes compétitions soit organisé.<br>
							L’arrivée aux JO ne va clairement pas améliorer la cohésion de tout ce monde là ! Les dissidents n’ont pas attendu longtemps pour se déchainer sur la toile et cracher leur haine du sport business qui dénature la « vrai pratique du skateboard ». 
							On peut comprendre leur point de vue, mais le Skateboard n’a pas attendu les JO pour rentrer dans ce monde là, les machines que sont les <a href="http://xgames.espn.com/" title="lien vers le site">X-Games</a> ou la <a href="http://streetleague.com/" title="lien vers le site">Street League Skateboarding</a> sont des vrais « usines à fric » aussi et sont quand même un énorme vecteur de communication qui a fortement participé au succès du Skateboard. 
						</p>
					</div>
				</li>
				<li>
					<div class="para_article">
						<h3>Les retombées des JO:</h3>
				
						<p>
							L’entrée aux JO, va faire changer de dimension les fédérations nationales, des retombées en termes de pratiquants, mais aussi financières, qui vont permettre d’investir dans l’encadrement (création de postes d’entraineurs, éducateurs, dirigeants, …) et d’infrastructures, avec de nouveaux skateparks (que tout le monde sera content de profiter !).
						</p>
