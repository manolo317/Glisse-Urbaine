								<h3>Montage avec une fourche à découper:</h3>
						
								<p>
									Pour pouvoir monter un scs sur une fourche pas adapter, il va falloir la couper pour, qu'elle ne dépasse pas du rebord où on place la rondelle qui va soutenir la vis pour compresser, et pour que l'on puisse y enfoncer l'étoile de compression.
									<br/>
									<br/>
									<strong>Astuce:</strong> On peut aussi mettre un spacer avant le scs, pour permettre de surélever le scs et donc de couper moins de fourche.
									<br/>
									Une fois la mesure faite avec attention, car une fois couper, les fourches ne repoussent pas!
									Pour couper la fourche, pas de secret, il faut une bonne scie à métaux et un établi ou un étau pour tenir la fourche et la tailler bien droite.
									Ensuite, il faut enfoncer une étoile de compression dans la fourche.
									<br/>
									<br/>
									<strong>Attention!!!</strong> l'étoile n'est pas la même que celle des <a href="ics.php">kits de compression ICS</a>, elle est plus petite en diamètre pour pouvoir passer dans la fourche. Vous en trouverez vendu dans la plupart des jeux de directions et souvent avec les SCS aussi (<strong>image 2</strong>).
									<br/>
									<br/>
									Pour enfoncer l'étoile, vous avez besoin d'un marteau, d'un tournevis relativement fin dont la lame rentre dans le trou de l'étoile. Positionnez l'étoile avec les branches recourbées vers vous (<strong>image 3</strong>), positionnez le tournevis dedans et frappez bien droit jusqu'à que l'étoile soit enfoncée complètement (image 4).
									Une fois le SCS mis en place, placer la rondelle et serrer la vis dans la fourche jusqu'à que cela force (<strong>image 5 et 6</strong>).
									Ensuite placez le guidon, avec ou sans le spacer, en fonction du diamètre de votre guidon.
									<br/>
									<br/>
									<strong>Astuce:</strong> Pour éviter de couper le guidon au niveau de la fente, vous pouvez mettre la chute de la fourche que vous avez coupé au niveau du bas du guidon, cela évitera que votre guidon se plie.
									<br/>
									<br/>
									Enfin, serrez bien les vis de serrage de votre scs, en vous assurant que votre guidon est bien droit.
