					<h3>Un SCS c'est quoi?</h3>
					<p>
						<strong>Standard Compression System</strong>, cette fois-ci, on va utiliser un collier de serrage spécial, qui va nous permettre de faire la compression avec la fourche. En effet, le collier de serrage SCS dispose d'un rebord à l'intérieur qui va nous permettre de caler une rondelle où va venir se caler une vis de compression, qui va se visser dans la fourche.
						Pour cela il faut comme dans tous les systèmes de compression, la fourche adaptée. La fourche doit ne pas dépasser du repère du rebord du SCS, où va se caler la rondelle pour la vis, pour laisser un espace entre la rondelle et la fourche, sinon on ne peut pas effectuer la compression (image 2). Ensuite elle doit être fileté directement pour pouvoir serrer a vis. Il est aussi possible de couper la fourche à la bonne hauteur et d'y insérer une étoile comme pour le HIC, mais cela demande du matériel et des compétences en bricolage, nous verrons ce montage plus loin. Si la fourche est un peu trop haute, il est aussi possible de mettre un spacer avant le SCS pour surélever  et atteindre la bonne hauteur, mais il est difficile de trouver un spacer assez solide et ayant le bon diamètre...
						La compression est efficace avec le SCS, mais il a tendance à bloquer la direction, surtout avec certains jeux de directions, dès que l'on serre trop.  
						Le SCS se compose du collier de serrage, d'une coupelle, d'une vis et d'un spacer (<strong>image 1</strong>). 
						<strong>Attention!!!</strong> Certain SCS, n'accepte que les guidons de diamètre 31.8 mm (Proto Baby ou Phoenix), donc ne pas oublier de vérifier le diamètre de son guidon avant d'acheter un SCS.
					</p>
