								<h3>Montage avec une fourche filetée:</h3>
	
						
								<p>
									Tout simplement, fixez le SCS comme un collier de serrage, le côté le plus étroit et celui que l'on va enfoncer dans la fourche (<strong>image 3</strong>). <br/>
									<br/>
									<strong>Astuce:</strong> parfois il rentre difficilement dans la fourche, donc pour éviter de galérer et de bourriner, dévissez complètement les vis de serrage, et revissez la vis de la base du SCS de l'autre côté (en commençant du côté fileté) et coincer une pièce de monnaie entre la vis et l'entrée du trou de la vis, comme cela ça va écarter le collier et il rentrera facilement dans la fourche.<br/>
									<br/>
									Une fois le SCS mis en place, placer la rondelle et serrer la vis dans la fourche jusqu'à que cela force (<strong>image 4 et 5</strong>).<br/>
									Ensuite placez le guidon, avec ou sans le spacer, en fonction du diamètre de votre guidon (<strong>image 6</strong>).<br/>
									<br/>
									<strong>Attention!!!</strong> Je vous conseille de couper le bout de votre guidon, si vous avez une fente, car le scs va plier le bout du guidon qui ne tiendra plus une fois serrer.<br/>
									<br/>
									Enfin, serrez bien les vis de serrage de votre scs, en vous assurant que votre guidon est bien droit.
									
